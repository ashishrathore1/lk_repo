package com.lk.credittemplate.beans.response;

/**
 * 
 * @author prashanth
 *
 */
public class ResponseEnquirySegment {
	
	private String dateofEnquiry="";
	private String enquiringName="";
	private String purpose="";
	private String amount="";
	/**
	 * @return the dateofEnquiry
	 */
	public String getDateofEnquiry() {
		return dateofEnquiry;
	}
	/**
	 * @param dateofEnquiry the dateofEnquiry to set
	 */
	public void setDateofEnquiry(String dateofEnquiry) {
		this.dateofEnquiry = dateofEnquiry;
	}
	/**
	 * @return the enquiringName
	 */
	public String getEnquiringName() {
		return enquiringName;
	}
	/**
	 * @param enquiringName the enquiringName to set
	 */
	public void setEnquiringName(String enquiringName) {
		this.enquiringName = enquiringName;
	}
	/**
	 * @return the purpose
	 */
	public String getPurpose() {
		return purpose;
	}
	/**
	 * @param purpose the purpose to set
	 */
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}
	/**
	 * @param amount the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	
}
