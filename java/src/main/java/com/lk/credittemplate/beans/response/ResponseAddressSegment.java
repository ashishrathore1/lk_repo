package com.lk.credittemplate.beans.response;

/**
 * 
 * @author prashanth
 *
 */
public class ResponseAddressSegment {
	
	private String address="";
	private String stateCode="";
	private String pinCode="";
	private String addressCategory="";
	private String residenceCode="";
	private String dateReported="";
	private String memberShortName="";
	private String enrichedThroughEnquiry="";
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the stateCode
	 */
	public String getStateCode() {
		return stateCode;
	}
	/**
	 * @param stateCode the stateCode to set
	 */
	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}
	/**
	 * @return the pinCode
	 */
	public String getPinCode() {
		return pinCode;
	}
	/**
	 * @param pinCode the pinCode to set
	 */
	public void setPinCode(String pinCode) {
		this.pinCode = pinCode;
	}
	/**
	 * @return the addressCategory
	 */
	public String getAddressCategory() {
		return addressCategory;
	}
	/**
	 * @param addressCategory the addressCategory to set
	 */
	public void setAddressCategory(String addressCategory) {
		this.addressCategory = addressCategory;
	}
	/**
	 * @return the residenceCode
	 */
	public String getResidenceCode() {
		return residenceCode;
	}
	/**
	 * @param residenceCode the residenceCode to set
	 */
	public void setResidenceCode(String residenceCode) {
		this.residenceCode = residenceCode;
	}
	/**
	 * @return the dateReported
	 */
	public String getDateReported() {
		return dateReported;
	}
	/**
	 * @param dateReported the dateReported to set
	 */
	public void setDateReported(String dateReported) {
		this.dateReported = dateReported;
	}
	/**
	 * @return the memberShortName
	 */
	public String getMemberShortName() {
		return memberShortName;
	}
	/**
	 * @param memberShortName the memberShortName to set
	 */
	public void setMemberShortName(String memberShortName) {
		this.memberShortName = memberShortName;
	}
	/**
	 * @return the enrichedThroughEnquiry
	 */
	public String getEnrichedThroughEnquiry() {
		return enrichedThroughEnquiry;
	}
	/**
	 * @param enrichedThroughEnquiry the enrichedThroughEnquiry to set
	 */
	public void setEnrichedThroughEnquiry(String enrichedThroughEnquiry) {
		this.enrichedThroughEnquiry = enrichedThroughEnquiry;
	}
	
	
}
